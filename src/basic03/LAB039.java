package src.basic03;

public class LAB039 {
    public static void main(String[] args) {

        // Increment operator
        // pre and post

        int a = 10;
        // a = a+1;
      //  a +=1;
        a++; //post increment - a = a+1
        ++a; // pre increment = a = a+1
        System.out.println(a);
    }
}
